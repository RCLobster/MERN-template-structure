const { Schema, model } = require('mongoose');

// Example of model schema for MongoDB

const modelSchema = new Schema({
  tech1: {
    type: String,
    required: true,
  },
  tech2: {
    type: String,
    required: true,
  },
  tech1_votes: {
    type: Number,
    default: 0,
  },
  tech2_votes: {
    type: Number,
    default: 0,
  },
});

const Model = model('Model', modelSchema);

module.exports = Model;
